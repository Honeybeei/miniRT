/* ************************************************************************** */

/**
 * @brief Writes n(2nd Parameter) zeroed bytes to the string s(1st Parameter). 
 * If n is zero, bzero() does nothing.
 * 
 * @param s 
 * @param n 
 */
void	ft_bzero(void *s, size_t n)
{
	ft_memset(s, 0, n);
}

