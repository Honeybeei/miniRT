/**
 * @brief Checks if the c(1st Parameter) is an alphabet.
 * 
 * @param c 
 * @return int 
 */
int	ft_isalpha(int c)
{
	if ('A' <= c && c <= 'Z')
		return (1);
	else if ('a' <= c && c <= 'z')
		return (1);
	return (0);
}

