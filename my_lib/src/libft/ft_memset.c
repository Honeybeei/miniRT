/**
 * @brief Writes len(3rd Parameter) bytes of value c(2nd Parameter)(converted to
 *  unsigned char) in string b(1st Parameter).
 * 
 * @param b 
 * @param c 
 * @param len 
 * @return void* 
 */
void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*dst;
	size_t			i;

	dst = (unsigned char *)b;
	i = 0;
	while (i < len)
	{
		dst[i] = (unsigned char)c;
		i++;
	}
	return (b);
}

