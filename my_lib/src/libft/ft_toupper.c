/**
 * @brief Convert a single character c(1st Parameter) to upper case if it is a 
 * lower case.
 * 
 * @param c 
 * @return int 
 */
int	ft_toupper(int c)
{
	if ('a' <= c && c <= 'z')
		c = c - 'a' + 'A';
	return (c);
}

