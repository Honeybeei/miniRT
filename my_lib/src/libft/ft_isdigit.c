/**
 * @brief Checks if c(1st Parameter) is a decimal digit character. 
 * 
 * @param c 
 * @return int 
 */
int	ft_isdigit(int c)
{
	if ('0' <= c && c <= '9')
		return (1);
	return (0);
}

