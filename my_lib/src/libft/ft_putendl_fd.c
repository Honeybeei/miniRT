/**
 * @brief Print the string  s(1st Parameter) to the given file descriptor 
 * followed by a newline. 
 * 
 * @param s 
 * @param fd 
 */
void	ft_putendl_fd(char *s, int fd)
{
	ft_putstr_fd(s, fd);
	write(fd, "\n", 1);
}

