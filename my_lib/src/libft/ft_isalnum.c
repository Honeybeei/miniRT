/**
 * @brief Check if the c(1st Parameter) is an alphabet or a decimal or a single 
 * digit character. 
 * 
 * @param c 
 * @return int 
 */
int	ft_isalnum(int c)
{
	if (ft_isalpha(c) || ft_isdigit(c))
		return (1);
	return (0);
}

